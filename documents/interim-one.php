<h2 id="planandschedule">Plan and schedule</h2>

<p>For this project the Three Way Methodology will be used. It is a methodology designed as a group activity during a WEPM tutorial. It allows for analysis, testing and prototyping and has approval at each stage. 

<figure>
<img src="../img/reports/intOneThreeWay.png"/>
<figcaption>The Three Way Methodology</figcaption></figure>

Based on the Gantt chart below, the planned lifecycle of this project will end approximately towards the end of February 2013. There are two reasons for this. Firstly, I believe that I am able to complete this project in this timescale, without affecting other work. Secondly, the planned timescale also allows enough time for any changes to the product as well as any issues that may arrise in completing the work on time. The main layout and basic functionality of the site will be completed by the 22nd of November with more advanced functionality being added after that. Some factors that are likely to affect work not being completed on time are:</p>

<ul>
<li>Other coursework taking priority temporarily

<ul>
<li>To avoid this, timescales for other coursework will be planned in such a way as to not affect this project.</li>
</ul></li>
<li>A particular function being difficult to complete</li>
<li>Learning new features of PHP and Javascript may be more difficult than initially planned for. This risk will be reduced by using all resources that are available:

<ul>
<li>Online tutorials</li>
<li>Books</li>
<li>One-to-one help in tutorial sessions</li>
</ul></li>
<li>Loss of work

<ul>
