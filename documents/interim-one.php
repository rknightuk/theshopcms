</ul></li>
</ul>

<p>The work that is being set in WEPM (Web Engineering and Project Management) will be useful for making sure the project is completed on time and meets all the requirements as outlined above. DBPRIN (Database Principles), WEBF2 (Web Foundations 2) and GUDE (Getting Usability into Development Environments) will also help with different elements of the project. The days outlined in the Gantt chart will not involve working the entirety of that particular day (8 hours), but instead are planned that work will be completed over a number of hours (3&#8211;4) of those particular days. </p>

